package academy.devdojo.maratonajava.calculator;

public class operations {
    public int addOperation(int n1, int n2) {
        return n1 + n2;
    }

    public int subOperation(int n1, int n2) {
        return n1 - n2;
    }

    public int mulOperation(int n1, int n2) {
        return n1 * n2;
    }

    public int divOperation(int n1, int n2) {
        return n1 / n2;
    }
}
