package academy.devdojo.maratonajava;

public class Aula04Operadores {
    public static void main(String[] args) {
        int n1 = 12;
        int n2 = 3;
        System.out.println(n1 + n2);
        System.out.println(n1 - n2);
        System.out.println(n1 * n2);
        System.out.println(n1 / n2);
    }
}
